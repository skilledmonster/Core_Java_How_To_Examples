package com.skilledmonster.examples.loops;

/**
 * Simple For Loop Example - display numbers between 1 to 10
 * 
 * This is an example to demonstrate simple for loop in java by displaying
 * numbers from 1 to 10 in sequence.
 * 
 * @author Jagadeesh
 * 
 */
public class SimpleForLoopExample {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {

			System.out.println("Number # " + i);
		}

	}

}
